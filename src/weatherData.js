// This function processes the data
const processData = async (data) => {
    try {
        const formattedData = {}; // Create an object to hold formatted data


        formattedData['current'] = { ...data.current }; // Format the data as needed
        
        const daily = await mappedObjs(data.daily)
        formattedData['daily'] = daily;
        const hourly = await mappedObjs(data.hourly)
        formattedData['hourly'] = hourly;

        console.log('Formatted Data:', formattedData);
        return JSON.stringify(formattedData); // Return the formatted data as a JSON string
    } catch (error) {
        console.error('processData error:', error);
    }
}

const mappedObjs = async(originalObj) => {
    const resultArray = [];

    // Get the maximum length of the arrays to ensure we cover all indices
    const maxLength = Math.max(...Object.values(originalObj).map(arr => arr.length));

    for (let i = 0; i < maxLength; i++) {
        const newObj = {};
        
        for (const prop in originalObj) {
            // Check if the current index exists in the array
            if (originalObj[prop][i] !== undefined) {
                newObj[prop] = originalObj[prop][i];
            }
        }
        
        // Only add the new object to the result if it has properties
        if (Object.keys(newObj).length > 0) {
            resultArray.push(newObj);
        }
    }
    
    return resultArray;
}

// This function exports the formatted data
export const formattedWeatherDataObj = async (apiData) => {
  const data = apiData;
    try {
        const jsonString = await processData(data); // Get the JSON string from processData
        return JSON.parse(jsonString); // Parse the JSON string into an object
    } catch (error) {
        console.error('formattedWeatherDataObj error:', error);
    }
}
  